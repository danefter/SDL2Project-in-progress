Map::Map()
{
	dirt = TextureManager::load_texture("assets/dirt.png");
	grass = TextureManager::load_texture("assets/grass.png");
	water = TextureManager::load_texture("assets/water.png");

	load_map(lvl1);

	src.x = src.y = 0;
	src.w = dest.w = 32;
	src.h = dest.h = 32;

	dest.x = dest.y = 0;
}

void Map::load_map(int array[20][25])
{
	for (int row =0; row < 20; row++)
	{
		for (int col = 0; col < 20; col++)
		{
			map[row][col] = array[row][col];
		}
	}
}
void Map::draw_map()
{
	int type = 0;
	for (int row = 0; row < 20; row++)
	{
		for (int col = 0; col < 20; col++)
		{
			type = map[row][col];

			dest.x = col * 32;
			dest.y = row * 32;

			switch (type)
			{
			case 0:
				TextureManager::draw(water, src, dest);
				break;
			case 1:
				TextureManager::draw(grass, src, dest);
				break;
			case 2:
				TextureManager::draw(dirt, src, dest);
				break;
			default:
					break;
			}
		}
	}
}

